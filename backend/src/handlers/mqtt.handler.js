/**
 * Log sensor reading to database
 */
async function logSensorReading(sensorType, value, unit, metadata = {}) {
  try {
    const db = await getDB();
    const collection = db.collection('sensor_logs');
    
    const document = {
      sensor_type: sensorType,
      value: value,
      unit: unit,
      device: metadata.device || 'BSF_001',
      timestamp: new Date(),
      createdAt: new Date()
    };

    const result = await collection.insertOne(document);
    console.log(`[MQTT Handler] ‚úÖ ${sensorType} logged: ${value}${unit}`);
    
    // Update current data
    await updateCurrentData({
      device: metadata.device || 'BSF_001',
      [sensorType.replace('bsf_', '')]: value
    });
    
    return result;
  } catch (error) {
    console.error(`[MQTT Handler] ‚ùå Error logging ${sensorType}:`, error.message);
  }
}

/**
 * Update current BSF data
 */
async function updateCurrentData(data) {
  try {
    const db = await getDB();
    const collection = db.collection('bsf_current_data');
    
    const document = {
      device: data.device || 'BSF_001',
      temperature: data.temperature || null,
      humidity: data.humidity || null,
      air_quality: data.air_quality || null,
      fan_state: data.fan_state || null,
      fan_mode: data.fan_mode || null,
      timestamp: new Date(),
      updatedAt: new Date()
    };

    const result = await collection.updateOne(
      { device: document.device },
      { $set: document },
      { upsert: true }
    );

    console.log('[MQTT Handler] ‚úÖ BSF current data updated');
    return result;
  } catch (error) {
    console.error('[MQTT Handler] ‚ùå Error updating BSF current data:', error.message);
  }
}

/**
 * Log control action
 */
async function logControlAction(action, metadata = {}) {
  try {
    const db = await getDB();
    const collection = db.collection('control_logs');
    
    const document = {
      action: action,
      device: metadata.device || 'BSF_001',
      state: metadata.state || null,
      mode: metadata.mode || null,
      timestamp: new Date(),
      createdAt: new Date()
    };

    const result = await collection.insertOne(document);
    console.log(`[MQTT Handler] ‚úÖ Control action logged: ${action}`);
    return result;
  } catch (error) {
    console.error('[MQTT Handler] ‚ùå Error logging control action:', error.message);
  }
}

